def generate_word_passphrase(word_count: int = 6, separator: str = "-") -> str:
    """Generate a word-based passphrase.

    Args:
        word_count: Number of words to include (default: 6)
        separator: Character to separate words (default: "-")

    Returns:
        A secure passphrase like "mountain-tiger-ocean-basket-rocket-palace"

    Example:
        >>> passphrase = generate_word_passphrase(6)
        >>> len(passphrase.split('-'))
        6
    """
    if word_count < 4:
        raise ValueError("Word count must be at least 4 for security")

    words = [secrets.choice(WORD_LIST) for _ in range(word_count)]
    return separator.join(words)


def generate_alphanumeric_passphrase(
    length: int = 24, include_symbols: bool = True
) -> str:
    """Generate a random alphanumeric passphrase with optional symbols.

    Args:
        length: Length of the passphrase (default: 24)
        include_symbols: Whether to include special symbols (default: True)

    Returns:
        A secure random passphrase like "xK9$mP2@qL5#vR8&nB3!"

    Example:
        >>> passphrase = generate_alphanumeric_passphrase(24)
        >>> len(passphrase)
        24
    """
    if length < 16:
        raise ValueError("Length must be at least 16 for security")

    alphabet = string.ascii_letters + string.digits
    if include_symbols:
        alphabet += "!@#$%^&*()-_=+[]{}|;:,.<>?"

    return "".join(secrets.choice(alphabet) for _ in range(length))


def generate_mixed_passphrase(word_count: int = 4, number_count: int = 4) -> str:
    """Generate a mixed passphrase with words and numbers.

    Args:
        word_count: Number of words to include (default: 4)
        number_count: Number of digits to append (default: 4)

    Returns:
        A secure passphrase like "tiger-ocean-basket-palace-9247"

    Example:
        >>> passphrase = generate_mixed_passphrase(4, 4)
        >>> parts = passphrase.rsplit('-', 1)
        >>> len(parts[0].split('-'))
        4
    """
    if word_count < 3:
        raise ValueError("Word count must be at least 3 for security")
    if number_count < 3:
        raise ValueError("Number count must be at least 3 for security")

    words = [secrets.choice(WORD_LIST) for _ in range(word_count)]
    numbers = "".join(secrets.choice(string.digits) for _ in range(number_count))

    return "-".join(words) + "-" + numbers


def calculate_entropy_bits(passphrase_type: str, **params) -> float:
    """Calculate the entropy (in bits) of a generated passphrase.

    Args:
        passphrase_type: Type of passphrase ('word', 'alphanumeric', 'mixed')
        **params: Parameters specific to the passphrase type

    Returns:
        Entropy in bits
    """
    import math

    if passphrase_type == "word":
        word_count = params.get("word_count", 6)
        return math.log2(len(WORD_LIST)) * word_count

    elif passphrase_type == "alphanumeric":
        length = params.get("length", 24)
        include_symbols = params.get("include_symbols", True)

        alphabet_size = len(string.ascii_letters) + len(string.digits)
        if include_symbols:
            alphabet_size += len("!@#$%^&*()-_=+[]{}|;:,.<>?")

        return math.log2(alphabet_size) * length

    elif passphrase_type == "mixed":
        word_count = params.get("word_count", 4)
        number_count = params.get("number_count", 4)

        word_entropy = math.log2(len(WORD_LIST)) * word_count
        number_entropy = math.log2(10) * number_count

        return word_entropy + number_entropy

    return 0.0


def generate_passphrase(
    strategy: str = "word",
    word_count: int = 6,
    length: int = 24,
    include_symbols: bool = True,
    number_count: int = 4,
) -> tuple[str, float]:
    """Generate a secure passphrase using the specified strategy.

    Args:
        strategy: Generation strategy ('word', 'alphanumeric', 'mixed')
        word_count: For word-based, number of words
        length: For alphanumeric, length of passphrase
        include_symbols: For alphanumeric, include special characters
        number_count: For mixed, number of digits to append

    Returns:
        Tuple of (passphrase, entropy_bits)

    Example:
        >>> passphrase, entropy = generate_passphrase('word', word_count=6)
        >>> entropy > 70  # Should have good entropy
        True
    """
    if strategy == "word":
        passphrase = generate_word_passphrase(word_count)
        entropy = calculate_entropy_bits("word", word_count=word_count)
    elif strategy == "alphanumeric":
        passphrase = generate_alphanumeric_passphrase(length, include_symbols)
        entropy = calculate_entropy_bits(
            "alphanumeric", length=length, include_symbols=include_symbols
        )
    elif strategy == "mixed":
        passphrase = generate_mixed_passphrase(word_count, number_count)
        entropy = calculate_entropy_bits(
            "mixed", word_count=word_count, number_count=number_count
        )
    else:
        raise ValueError(
            f"Unknown strategy: {strategy}. Use 'word', 'alphanumeric', or 'mixed'"
        )

    return passphrase, entropy
